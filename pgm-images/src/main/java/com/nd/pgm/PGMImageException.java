package com.nd.pgm;

public class PGMImageException extends Exception {

	private static final long serialVersionUID = -3306607690394897946L;

	public PGMImageException() {
	}

	public PGMImageException(String arg0) {
		super(arg0);
	}

	public PGMImageException(Throwable arg0) {
		super(arg0);
	}

	public PGMImageException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
